      email: '',
      modal: false,
      transfer: true,
      emailStatus: true
    };
  },
  methods: {
    closeModal() {
      this.modal = false
    },
    @track(
      after(function() {
        Track.eventTrack({
          category: "loginout-dropdown",
          action: "click",
          optLabel: "dropdown",
          page: this.$route.name,
          optValue: {
            tabName: this.tabName
          }
        });
      })
